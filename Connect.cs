using EnvDTE;
using EnvDTE80;
using Microsoft.VisualStudio.Shell;

namespace SokoolTools.VsTools
{
    //----------------------------------------------------------------------------------------------------------------------------
    /// <summary>
    /// 
    /// </summary>
    //----------------------------------------------------------------------------------------------------------------------------
    public static class Connect
    {
        //------------------------------------------------------------------------------------------------------------------------
        /// <summary>
        /// The DTE object.
        /// </summary>
        //------------------------------------------------------------------------------------------------------------------------
        public static readonly DTE2 ApplicationObject;

		//------------------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Initializes the <see cref="Connect"/> class.
		/// </summary>
		//------------------------------------------------------------------------------------------------------------------------
		static Connect()
        {
            ThreadHelper.ThrowIfNotOnUIThread();
            ApplicationObject = (DTE2)Package.GetGlobalService(typeof(DTE));
		}
	}
}
